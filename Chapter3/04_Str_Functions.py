name = "Aanisha"

print(len(name)) # give total length of the string

print(name.endswith("isha")) # true or false give that the given end character string ends with given string or not
# output = True

print(name.startswith("San")) # true or false give that the given start string starts with the given string or not.
# output = False

print(name.capitalize()) # capitalize the first character of string only 
# output = Manisha

print(name.upper()) # To convert the whole string in Upper Case.
# output = AANISHA

print(name.isalnum())

